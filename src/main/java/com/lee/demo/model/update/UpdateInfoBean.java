package com.lee.demo.model.update;

public class UpdateInfoBean {
    private boolean couldUpdate;
    private boolean forceUpdate;
    private String updateTips;
    private String appVersion;
    private String appSize;
    private String fileSha1;
    private String updateUrl;

    public boolean isCouldUpdate() {
        return couldUpdate;
    }

    public void setCouldUpdate(boolean couldUpdate) {
        this.couldUpdate = couldUpdate;
    }

    public boolean isForceUpdate() {
        return forceUpdate;
    }

    public void setForceUpdate(boolean forceUpdate) {
        this.forceUpdate = forceUpdate;
    }

    public String getUpdateTips() {
        return updateTips;
    }

    public void setUpdateTips(String updateTips) {
        this.updateTips = updateTips;
    }

    public String getAppVersion() {
        return appVersion;
    }

    public void setAppVersion(String appVersion) {
        this.appVersion = appVersion;
    }

    public String getAppSize() {
        return appSize;
    }

    public void setAppSize(String appSize) {
        this.appSize = appSize;
    }

    public String getFileSha1() {
        return fileSha1;
    }

    public void setFileSha1(String fileSha1) {
        this.fileSha1 = fileSha1;
    }

    public String getUpdateUrl() {
        return updateUrl;
    }

    public void setUpdateUrl(String updateUrl) {
        this.updateUrl = updateUrl;
    }
}
